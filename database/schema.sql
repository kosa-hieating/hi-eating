DECLARE
    TABLE_MISSING EXCEPTION;
    PRAGMA EXCEPTION_INIT (TABLE_MISSING, -942);

    PROCEDURE DROP_TABLE_IF_EXISTS(P_TABLE_NAME IN VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE 'DROP TABLE ' || P_TABLE_NAME || ' CASCADE CONSTRAINTS PURGE';
    EXCEPTION
        WHEN TABLE_MISSING THEN
            NULL;
    END;
BEGIN
    DROP_TABLE_IF_EXISTS('email_retry_logs');
    DROP_TABLE_IF_EXISTS('email_send_logs');
    DROP_TABLE_IF_EXISTS('hot_deal_products');
    DROP_TABLE_IF_EXISTS('hot_deals');
    DROP_TABLE_IF_EXISTS('reviews');
    DROP_TABLE_IF_EXISTS('purchase_product_options');
    DROP_TABLE_IF_EXISTS('purchases');
    DROP_TABLE_IF_EXISTS('visits');
    DROP_TABLE_IF_EXISTS('favorites');
    DROP_TABLE_IF_EXISTS('product_pictures');
    DROP_TABLE_IF_EXISTS('product_options');
    DROP_TABLE_IF_EXISTS('products');
    DROP_TABLE_IF_EXISTS('promotions');
    DROP_TABLE_IF_EXISTS('user_auth');
    DROP_TABLE_IF_EXISTS('categories');
    DROP_TABLE_IF_EXISTS('users');
    DROP_TABLE_IF_EXISTS('posts');
    DROP_TABLE_IF_EXISTS('likes');
END;
/

CREATE TABLE USERS
(
    ID         NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    EMAIL      VARCHAR2(255)                  NOT NULL,
    PASSWORD   VARCHAR2(255)                  NOT NULL,
    NAME       VARCHAR2(100)                  NOT NULL,
    GENDER     VARCHAR2(20)                   NOT NULL,
    BIRTH      DATE                           NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    CONSTRAINT PK_USERS PRIMARY KEY (ID),
    CONSTRAINT UK_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CHK_USERS_GENDER CHECK (GENDER IN ('MALE', 'FEMALE', 'OTHER'))
);

CREATE TABLE POSTS
(
    ID         NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID    NUMBER(19)                     NOT NULL,
    LIKES      NUMBER,
    IMG_SRC    VARCHAR2(1000)                 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    CONSTRAINT PK_POSTS PRIMARY KEY (ID),
    CONSTRAINT FK_POSTS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID)
);

CREATE TABLE LIKES
(
    ID         NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID    NUMBER(19)                     NOT NULL,
    POST_ID    NUMBER(19)                     NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    CONSTRAINT PK_LIKES PRIMARY KEY (ID),
    CONSTRAINT UK_LIKES_USER_POST UNIQUE (USER_ID, POST_ID),
    CONSTRAINT FK_LIKES_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_LIKES_POST FOREIGN KEY (POST_ID) REFERENCES POSTS (ID) ON DELETE CASCADE
);

CREATE TABLE USER_AUTH
(
    AUTH_NO    NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID    NUMBER(19)                     NOT NULL,
    AUTH       VARCHAR2(50)                   NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT PK_USER_AUTH PRIMARY KEY (AUTH_NO),
    CONSTRAINT UK_USER_AUTH UNIQUE (USER_ID, AUTH),
    CONSTRAINT FK_UA_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT CHK_UA_AUTH CHECK (AUTH IN ('ROLE_USER', 'ROLE_ADMIN'))
);

CREATE TABLE CATEGORIES
(
    ID         NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NAME       VARCHAR2(100)                  NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT PK_CATEGORIES PRIMARY KEY (ID),
    CONSTRAINT UK_CATEGORIES_NAME UNIQUE (NAME)
);

CREATE TABLE PRODUCTS
(
    ID          NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CATEGORY_ID NUMBER(19)                        NOT NULL,
    NAME        VARCHAR2(255)                     NOT NULL,
    DESCRIPTION CLOB,
    PRICE       NUMBER(10)                        NOT NULL,
    VIEW_COUNT  NUMBER(10)   DEFAULT 0            NOT NULL,
    STATUS      VARCHAR2(30) DEFAULT 'ON_SALE'    NOT NULL,
    CREATED_AT  TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP,
    CONSTRAINT PK_PRODUCTS PRIMARY KEY (ID),
    CONSTRAINT FK_PRODUCTS_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES (ID),
    CONSTRAINT CHK_PRODUCTS_PRICE CHECK (PRICE >= 0),
    CONSTRAINT CHK_PRODUCTS_VIEW CHECK (VIEW_COUNT >= 0),
    CONSTRAINT CHK_PRODUCTS_STATUS CHECK (STATUS IN ('ON_SALE', 'SOLD_OUT', 'STOPPED'))
);

CREATE TABLE PRODUCT_PICTURES
(
    ID                     NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PRODUCT_ID             NUMBER(19)                      NOT NULL,
    PICTURE_SAVED_LOCATION VARCHAR2(1000)                  NOT NULL,
    DISPLAY_ORDER          NUMBER(10) DEFAULT 1            NOT NULL,
    CREATED_AT             TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT             TIMESTAMP,

    CONSTRAINT PK_PRODUCT_PICTURES PRIMARY KEY (ID),
    CONSTRAINT FK_PICTURES_PRODUCTS FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID) ON DELETE CASCADE,
    CONSTRAINT UK_PRODUCT_PICTURES_LOCATION UNIQUE (PRODUCT_ID, PICTURE_SAVED_LOCATION),
    CONSTRAINT UK_PRODUCT_PICTURES_ORDER UNIQUE (PRODUCT_ID, DISPLAY_ORDER),
    CONSTRAINT CHK_PRODUCT_PICTURES_ORDER CHECK (DISPLAY_ORDER > 0)
);

CREATE TABLE PRODUCT_OPTIONS
(
    ID          NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PRODUCT_ID  NUMBER(19)                      NOT NULL,
    STOCK       NUMBER(10) DEFAULT 0            NOT NULL,
    EXPIRE_DATE DATE                            NOT NULL,
    CREATED_AT  TIMESTAMP  DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP,
    CONSTRAINT PK_PRODUCT_OPTIONS PRIMARY KEY (ID),
    CONSTRAINT FK_OPTIONS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID) ON DELETE CASCADE,
    CONSTRAINT CHK_OPTIONS_STOCK CHECK (STOCK >= 0)
);

CREATE TABLE FAVORITES
(
    USER_ID    NUMBER(19)                     NOT NULL,
    PRODUCT_ID NUMBER(19)                     NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT PK_FAVORITES PRIMARY KEY (USER_ID, PRODUCT_ID),
    CONSTRAINT FK_FAVORITES_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_FAVORITES_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID) ON DELETE CASCADE
);

CREATE TABLE VISITS
(
    USER_ID    NUMBER(19)                     NOT NULL,
    PRODUCT_ID NUMBER(19)                     NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT PK_VISITS PRIMARY KEY (USER_ID, PRODUCT_ID),
    CONSTRAINT FK_VISITS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_VISITS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID) ON DELETE CASCADE
);

CREATE TABLE PURCHASES
(
    ID             NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID        NUMBER(19)                     NOT NULL,
    PRODUCT_ID     NUMBER(19)                     NOT NULL,
    QUANTITY       NUMBER(10)                     NOT NULL,
    PURCHASE_PRICE NUMBER(10)                     NOT NULL,
    TOTAL_PRICE    NUMBER(30) GENERATED ALWAYS AS (QUANTITY * PURCHASE_PRICE) VIRTUAL,
    CREATED_AT     TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT     TIMESTAMP,
    CONSTRAINT PK_PURCHASES PRIMARY KEY (ID),
    CONSTRAINT FK_PURCHASES_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT FK_PURCHASES_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID),
    CONSTRAINT CHK_PURCHASES_QTY CHECK (QUANTITY > 0),
    CONSTRAINT CHK_PURCHASES_PRICE CHECK (PURCHASE_PRICE >= 0)
);

CREATE TABLE PURCHASE_PRODUCT_OPTIONS
(
    PURCHASE_ID       NUMBER(19)                     NOT NULL,
    PRODUCT_OPTION_ID NUMBER(19)                     NOT NULL,
    QUANTITY          NUMBER(10)                     NOT NULL,
    CREATED_AT        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT        TIMESTAMP,
    CONSTRAINT PK_PURCHASE_PRODUCT_OPTIONS PRIMARY KEY (PURCHASE_ID, PRODUCT_OPTION_ID),
    CONSTRAINT FK_PPO_PURCHASE FOREIGN KEY (PURCHASE_ID) REFERENCES PURCHASES (ID) ON DELETE CASCADE,
    CONSTRAINT FK_PPO_OPTION FOREIGN KEY (PRODUCT_OPTION_ID) REFERENCES PRODUCT_OPTIONS (ID),
    CONSTRAINT CHK_PPO_QTY CHECK (QUANTITY > 0)
);

CREATE TABLE REVIEWS
(
    ID          NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID     NUMBER(19)                     NOT NULL,
    PRODUCT_ID  NUMBER(19)                     NOT NULL,
    PURCHASE_ID NUMBER(19)                     NOT NULL,
    RATING      NUMBER(1)                      NOT NULL,
    CONTENT     CLOB,
    IMG_SRC     VARCHAR2(1000),
    CREATED_AT  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP,
    DELETED_AT  TIMESTAMP,
    CONSTRAINT PK_REVIEWS PRIMARY KEY (ID),
    CONSTRAINT UK_REVIEWS_PURCHASE UNIQUE (PURCHASE_ID),
    CONSTRAINT FK_REVIEWS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT FK_REVIEWS_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS (ID),
    CONSTRAINT FK_REVIEWS_PURCHASE FOREIGN KEY (PURCHASE_ID) REFERENCES PURCHASES (ID),
    CONSTRAINT CHK_REVIEWS_RATING CHECK (RATING BETWEEN 1 AND 5)
);

CREATE TABLE HOT_DEALS
(
    ID          NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CREATED_BY  NUMBER(19)                        NOT NULL,
    TITLE       VARCHAR2(255)                     NOT NULL,
    DESCRIPTION CLOB,
    STARTS_AT   TIMESTAMP                         NOT NULL,
    ENDS_AT     TIMESTAMP                         NOT NULL,
    STATUS      VARCHAR2(30) DEFAULT 'SCHEDULED'  NOT NULL,
    CREATED_AT  TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP,
    DELETED_AT  TIMESTAMP,
    CONSTRAINT PK_HOT_DEALS PRIMARY KEY (ID),
    CONSTRAINT FK_HOT_DEALS_USER FOREIGN KEY (CREATED_BY) REFERENCES USERS (ID),
    CONSTRAINT CHK_HOT_DEALS_STATUS CHECK (STATUS IN ('SCHEDULED', 'ACTIVE', 'ENDED', 'DELETED')),
    CONSTRAINT CHK_HOT_DEALS_PERIOD CHECK (ENDS_AT > STARTS_AT)
);

CREATE TABLE HOT_DEAL_PRODUCTS
(
    HOT_DEAL_ID       NUMBER(19)                     NOT NULL,
    PRODUCT_OPTION_ID NUMBER(19)                     NOT NULL,
    ORIGINAL_PRICE    NUMBER(10)                     NOT NULL,
    HOT_DEAL_PRICE    NUMBER(10)                     NOT NULL,
    CREATED_AT        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_HOT_DEAL_PRODUCTS PRIMARY KEY (HOT_DEAL_ID, PRODUCT_OPTION_ID),
    CONSTRAINT FK_HDP_HOT_DEAL FOREIGN KEY (HOT_DEAL_ID) REFERENCES HOT_DEALS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_HDP_PRODUCT FOREIGN KEY (PRODUCT_OPTION_ID) REFERENCES PRODUCT_OPTIONS (ID),
    CONSTRAINT CHK_HDP_PRICE CHECK (ORIGINAL_PRICE >= 0 AND HOT_DEAL_PRICE >= 0 AND
                                    HOT_DEAL_PRICE <= ORIGINAL_PRICE)
);

CREATE TABLE EMAIL_SEND_LOGS
(
    ID              NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    HOT_DEAL_ID     NUMBER(19),
    USER_ID         NUMBER(19)                        NOT NULL,
    RECIPIENT_EMAIL VARCHAR2(255)                     NOT NULL,
    TITLE           VARCHAR2(255)                     NOT NULL,
    CONTENT         CLOB,
    STATUS          VARCHAR2(30) DEFAULT 'PENDING'    NOT NULL,
    RETRY_COUNT     NUMBER(5)    DEFAULT 0            NOT NULL,
    FAILURE_REASON  CLOB,
    CREATED_AT      TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP,
    CONSTRAINT PK_EMAIL_SEND_LOGS PRIMARY KEY (ID),
    CONSTRAINT FK_ESL_HOT_DEAL FOREIGN KEY (HOT_DEAL_ID) REFERENCES HOT_DEALS (ID),
    CONSTRAINT FK_ESL_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID),
    CONSTRAINT CHK_ESL_STATUS CHECK (STATUS IN ('PENDING',
                                                'NEEDS_REVIEW',
                                                'APPROVED',
                                                'PUBLISHED',
                                                'SENDING',
                                                'SENT',
                                                'RETRYING',
                                                'FAILED')),
    CONSTRAINT CHK_ESL_RETRY CHECK (RETRY_COUNT >= 0)
);

CREATE TABLE EMAIL_RETRY_LOGS
(
    ID                NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    EMAIL_SEND_LOG_ID NUMBER(19)                     NOT NULL,
    RETRY_COUNT       NUMBER(5)                      NOT NULL,
    FAILURE_REASON    CLOB                           NOT NULL,
    CREATED_AT        TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_EMAIL_RETRY_LOGS PRIMARY KEY (ID),
    CONSTRAINT FK_ERL_SEND_LOG FOREIGN KEY (EMAIL_SEND_LOG_ID) REFERENCES EMAIL_SEND_LOGS (ID) ON DELETE CASCADE,
    CONSTRAINT CHK_ERL_RETRY CHECK (RETRY_COUNT > 0)
);

CREATE TABLE PROMOTIONS
(
    ID            NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    TITLE         VARCHAR2(255)                  NOT NULL,
    IMG_SRC       VARCHAR2(1000)                 NOT NULL,
    LINK          VARCHAR2(1000),
    DISPLAY_ORDER NUMBER(10)                     NOT NULL,
    STARTS_AT     TIMESTAMP                      NOT NULL,
    ENDS_AT       TIMESTAMP                      NOT NULL,
    CREATED_AT    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT    TIMESTAMP,
    CONSTRAINT PK_PROMOTIONS PRIMARY KEY (ID),
    CONSTRAINT UK_PROMOTIONS_ORDER UNIQUE (DISPLAY_ORDER),
    CONSTRAINT CHK_PROMOTIONS_ORDER CHECK (DISPLAY_ORDER > 0),
    CONSTRAINT CHK_PROMOTIONS_PERIOD CHECK (ENDS_AT > STARTS_AT)
);

CREATE INDEX IDX_PRODUCT_CATEGORY ON PRODUCTS (CATEGORY_ID);
