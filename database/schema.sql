-- 1:1 고객 상담 채팅방. 사용자별 활성 채팅방을 하나만 유지한다.
CREATE TABLE CHAT_ROOMS
(
    ID                 NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID            NUMBER(19)                     NOT NULL,
    ASSIGNED_ADMIN_ID  NUMBER(19),
    STATUS             VARCHAR2(20) DEFAULT 'OPEN'    NOT NULL,
    USER_UNREAD_COUNT  NUMBER(10)   DEFAULT 0         NOT NULL,
    ADMIN_UNREAD_COUNT NUMBER(10)   DEFAULT 0         NOT NULL,
    LAST_MESSAGE_AT    TIMESTAMP,
    CREATED_AT         TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT         TIMESTAMP,
    CONSTRAINT PK_CHAT_ROOMS PRIMARY KEY (ID),
    CONSTRAINT UK_CHAT_ROOMS_USER UNIQUE (USER_ID),
    CONSTRAINT FK_CHAT_ROOMS_USER FOREIGN KEY (USER_ID) REFERENCES USERS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_CHAT_ROOMS_ADMIN FOREIGN KEY (ASSIGNED_ADMIN_ID) REFERENCES USERS (ID),
    CONSTRAINT CHK_CHAT_ROOMS_STATUS CHECK (STATUS IN ('OPEN', 'CLOSED')),
    CONSTRAINT CHK_CHAT_ROOMS_USER_UNREAD CHECK (USER_UNREAD_COUNT >= 0),
    CONSTRAINT CHK_CHAT_ROOMS_ADMIN_UNREAD CHECK (ADMIN_UNREAD_COUNT >= 0)
);

